import { View, Text, TouchableOpacity, Image } from "react-native";
import React from "react";
import { HeaderProps } from "@/constants/types";
import { Ionicons } from "@expo/vector-icons";
import { COLORS } from "@/constants";
import { useRouter } from "expo-router";

export default function Header({
  title,
  showBack,
  showSearch,
  showCart,
  showMenu,
  showLogo,
}: HeaderProps) {
  const router = useRouter();
  const itemCount = 6;
  return (
    <View className="flex-row items-center justify-between px-4 py-3 bg-white">
      {/* left side*/}
      <View className="flex-row items-center flex-1">
        {showBack && (
          <TouchableOpacity onPress={() => router.back()} className="mr-3">
            <Ionicons name="arrow-back" size={24} color={COLORS.primary} />
          </TouchableOpacity>
        )}

        {showMenu && (
          <TouchableOpacity className="mr-3">
            <Ionicons name="menu-outline" size={28} color={COLORS.primary} />
          </TouchableOpacity>
        )}

        {showLogo ? (
          <View className="flex-1">
            <Image
              source={require("@/assets/logo.png")}
              style={{
                width: "100%",
                height: 24,
                resizeMode: "contain",
              }}
            />
          </View>
        ) : (
          title && (
            <Text className="text-xl font-bold text-primary text-center flex-1 mr-8">
              {title}
            </Text>
          )
        )}

        {!title && !showSearch && <View className="flex-1" />}
      </View>
      {/* right side*/}
      <View className="flex-row items-center gap-4">
        {showSearch && (
          <TouchableOpacity>
            <Ionicons name="search-outline" size={24} color={COLORS.primary} />
          </TouchableOpacity>
        )}

        {showCart && (
          <TouchableOpacity
            onPress={() => router.push("/(tabs)/Cart")}
            className="p-1"
          >
            <View className="relative">
              <Ionicons name="bag-outline" size={24} color={COLORS.primary} />

              {itemCount > 0 && (
                <View
                  className="absolute items-center justify-center bg-accent rounded-full"
                  style={{
                    top: -6,
                    right: -8,
                    maxWidth: 20,
                    width: "100%",
                    height: 16,
                    paddingHorizontal: 3,
                  }}
                >
                  <Text
                    className="text-white font-bold"
                    style={{ fontSize: 8, lineHeight: 8 }}
                    numberOfLines={1}
                  >
                    {itemCount > 99 ? "99+" : itemCount}
                  </Text>
                </View>
              )}
            </View>
          </TouchableOpacity>
        )}
      </View>
    </View>
  );
}
